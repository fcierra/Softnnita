ENGINE=InnoDB
AUTO_INCREMENT=0
;



CREATE TABLE ISOFT_MENUS_PERFIL (
	ID_MENU_PERFIL BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,	
	ID_MENU  BIGINT(20) UNSIGNED NULL DEFAULT NULL,
