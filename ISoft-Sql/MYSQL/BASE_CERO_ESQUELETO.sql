	HABILITADO TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'logico que habilita / deshabilita el registro val: 0,1',		
	PRIMARY KEY (COD_IDIOMA),
	UNIQUE INDEX ID_IDIOMA (ID_IDIOMA)	
)
